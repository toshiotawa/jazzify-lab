/**
 * ランクから対応するランク情報を取得
 */
export function getFantasyRankInfo(rank: number, tier: 'basic' | 'advanced' | 'phrases' = 'basic', isEnglish: boolean = false): FantasyRankInfo {
  // 1-200の範囲に収める
  const validRank = Math.min(Math.max(rank, 1), 200);
  // tierに応じたリストを選択
  const list = tier === 'advanced' 
    ? FANTASY_RANKS_ADVANCED 
    : tier === 'phrases'
    ? FANTASY_RANKS_PHRASES
    : FANTASY_RANKS_BASIC;
  
  const info = list[validRank - 1];

  if (isEnglish) {
    const enMap = tier === 'advanced' 
      ? FANTASY_RANKS_ADVANCED_EN 
      : tier === 'phrases'
      ? FANTASY_RANKS_PHRASES_EN
      : FANTASY_RANKS_BASIC_EN;
    
    const enInfo = enMap[validRank];
    if (enInfo) {
      return { ...info, ...enInfo };
    }
    // If no specific translation, use generic
    return { ...info, ...getGenericRankTranslation(validRank, tier) };
  }

  return info;
}

/**
 * クリア済みステージ数から現在のランクを計算
 * 10ステージクリアごとに1ランクアップ
 */
export function getRankFromClearedStages(clearedStages: number): number {
  return Math.floor(clearedStages / 10) + 1;
}

/**
 * 数値ランクをウィザードランク文字列に変換（互換性のため）
 * 1 -> F, 2 -> F+, 3 -> E, 4 -> E+, ...
 */
export function getWizardRankString(clearedStages: number): string {
  const WIZARD_RANKS = ['F', 'F+', 'E', 'E+', 'D', 'D+', 'C', 'C+', 'B', 'B+', 'A', 'A+', 'S', 'S+'];
  const rankIndex = Math.floor(clearedStages / 10);
  return WIZARD_RANKS[Math.min(rankIndex, WIZARD_RANKS.length - 1)];
}

