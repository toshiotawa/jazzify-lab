/**
 * ステージ番号からランク（1-200）を取得
 */
export function getRankFromStageNumber(stageNumber: string): number {
  // ステージ番号は "1-1", "1-2" のような形式
  const rank = parseInt(stageNumber.split('-')[0], 10);
  return isNaN(rank) ? 1 : Math.min(Math.max(rank, 1), 200);
}

