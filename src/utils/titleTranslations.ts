/**
 * 称号を英語に翻訳する関数
 * 翻訳がない場合は元の称号を返す
 */
export function translateTitle(title: string, isEnglish: boolean): string {
  if (!isEnglish) {
    return title;
  }
  return TITLE_TRANSLATIONS_EN[title] || title;
}

/**
 * 称号の取得条件を英語に翻訳する関数
 */
export function translateTitleRequirement(requirement: string, isEnglish: boolean): string {
  if (!isEnglish) {
    return requirement;
  }

  // パターンマッチングで翻訳
  if (requirement.includes('ミッション')) {
    const match = requirement.match(/ミッション(\d+)個完了で獲得/);
    if (match) {
      return `Earned by completing ${match[1]} missions`;
    }
  }

  if (requirement.includes('レッスン')) {
    const match = requirement.match(/レッスン(\d+)個完了で獲得/);
    if (match) {
      return `Earned by completing ${match[1]} lessons`;
    }
  }

  if (requirement.includes('レベル')) {
    const match = requirement.match(/レベル(\d+)で獲得/);
    if (match) {
      return `Earned at Level ${match[1]}`;
    }
    if (requirement.includes('初期称号')) {
      return 'Earned at Level 0 (initial title)';
    }
  }

  if (requirement.includes('ファンタジーモード')) {
    const match = requirement.match(/ファンタジーモード(\d+)ステージクリアで獲得/);
    if (match) {
      return `Earned by clearing ${match[1]} Fantasy Mode stages`;
    }
    if (requirement.includes('初期称号')) {
      return 'Fantasy Mode initial title';
    }
  }

  if (requirement.includes('アドバンスドモード')) {
    const match = requirement.match(/アドバンスドモード(\d+)ステージクリアで獲得/);
    if (match) {
      return `Earned by clearing ${match[1]} Advanced Mode stages`;
    }
    if (requirement.includes('初期称号')) {
      return 'Advanced Mode initial title';
    }
  }

  if (requirement.includes('フレーズモード')) {
    const match = requirement.match(/フレーズモード(\d+)ステージクリアで獲得/);
    if (match) {
      return `Earned by clearing ${match[1]} Phrase Mode stages`;
    }
    if (requirement.includes('初期称号')) {
      return 'Phrase Mode initial title';
    }
  }

  if (requirement === '条件不明') {
    return 'Unknown requirement';
  }

  return requirement;
}
